    <script>
        document.addEventListener("DOMContentLoaded", () => {
            const regionItems = document.querySelectorAll(".region-area ul li");
            const destLists = document.querySelectorAll(".destlist");

            regionItems.forEach(item => {
                item.addEventListener("click", () => {
                    const region = item.getAttribute("data-region");

                    // Remove active from all and add to clicked
                    regionItems.forEach(li => li.classList.remove("active"));
                    item.classList.add("active");

                    // Change destination list smoothly
                    destLists.forEach(list => {
                        if (list.getAttribute("data-region") === region) {
                            list.classList.add("active");
                        } else {
                            list.classList.remove("active");
                        }
                    });
                });
            });
        });
    </script>

    <script>
        (function() {
            let isSubmitting = false;
            const form = document.getElementById('contactForm');
            if (!form) return;
            
            form.addEventListener('submit', function(e) {
                e.preventDefault();
                e.stopPropagation();
                e.stopImmediatePropagation();
                
                if (isSubmitting) {
                    console.log('Ya se está enviando, ignorando...');
                    return false;
                }
                
                isSubmitting = true;
                
                const button = this.querySelector('button[type="submit"]');
                const inputs = this.querySelectorAll('input, textarea, select');
                const formElement = this;
                
                // Capturar FormData PRIMERO
                const formData = new FormData(formElement);
                
                // Deshabilitar para UX
                if (button) {
                    button.disabled = true;
                    button.dataset.originalHtml = button.innerHTML;
                    button.innerHTML = 'Enviando...';
                }
                inputs.forEach(input => input.disabled = true);
                
                // Ejecutar reCAPTCHA v3
                grecaptcha.ready(function() {
                    grecaptcha.execute('6LeIjDwsAAAAALJ4tgYzJkcZeO0ut5RShcdH6p9X', {action: 'submit'}).then(function(token) {
                        formData.append('g-recaptcha-response', token);
                        
                        fetch('send_contact.php', {
                            method: 'POST',
                            body: formData
                        })
                        .then(response => response.text())
                        .then(data => {
                            if (data.includes('exitosamente')) {
                                Swal.fire({
                                    icon: 'success',
                                    title: '¡Mensaje enviado!',
                                    text: 'Tu mensaje ha sido enviado exitosamente. Revisa tu correo para la confirmación.',
                                    confirmButtonText: 'Aceptar'
                                });
                                formElement.reset();
                            } else {
                                Swal.fire({
                                    icon: 'error',
                                    title: 'Error',
                                    text: data,
                                    confirmButtonText: 'Aceptar'
                                });
                            }
                        })
                        .catch(error => {
                            Swal.fire({
                                icon: 'error',
                                title: 'Error',
                                text: 'Hubo un problema al enviar el mensaje.',
                                confirmButtonText: 'Aceptar'
                            });
                        })
                        .finally(() => {
                            isSubmitting = false;
                            if (button) {
                                button.disabled = false;
                                button.innerHTML = button.dataset.originalHtml || 'Enviar <i class="fa-regular fa-arrow-up up-right"></i>';
                            }
                            inputs.forEach(input => input.disabled = false);
                        });
                    }).catch(function(error) {
                        isSubmitting = false;
                        if (button) {
                            button.disabled = false;
                            button.innerHTML = button.dataset.originalHtml || 'Enviar <i class="fa-regular fa-arrow-up up-right"></i>';
                        }
                        inputs.forEach(input => input.disabled = false);
                        Swal.fire({
                            icon: 'error',
                            title: 'Error',
                            text: 'Error de verificación de seguridad. Por favor intenta nuevamente.',
                            confirmButtonText: 'Aceptar'
                        });
                    });
                });
                
                return false;
            }, true);
        })();
    </script>

